% Отношение: duplicate_elements(+Source, -Target)

% Базовый случай: пустой список
duplicate_elements([], []).

% Рекурсивный случай: удвоение головы и обработка хвоста
duplicate_elements([H | T], [H, H | Rest]) :- duplicate_elements(T, Rest).

% Запрос и результат
?- duplicate_elements([a, b, c], Result).
Result = [a, a, b, b, c, c].

/*
Декларативная интерпретация:
Существует такой список Result, который получен из списка [a, b, c] путём удвоения каждого элемента.

Процедурная интерпретация:
[a, b, c] не пуст → применяем рекурсивное правило.
Голова a записывается дважды, затем рекурсивно обрабатывается [b, c].
Для [b, c] голова b записывается дважды, рекурсивно обрабатывается [c].
Для [c] голова c записывается дважды, рекурсивно обрабатывается [].
Для [] базовый случай возвращает [].
Результат собирается: [a, a, b, b, c, c].
*/
